public class Computer
{
    private int screenSize;
    private int memory;
    
    public int getScreenSize()
    {
        return screenSize;
    }
    public void setScreenSize(int newSize)
    {
        screenSize = newSize;
    }
    
    public int getMemory()
    {
        return memory;
    }
    public void setMemory(int newMemory)
    {
        memory = newMemory;
    }
    
    
    
}
