public class Pet extends Animal
{
    private String name;
    private String size;
    
    public String getName()
    {
        return name;
    }
    public void setName(String newName)
    {
        name = newName;
    }
    public String getSize()
    {
        return size;
    }
    public void setSize(String newSize)
    {
        size = newSize;
    }
   
}
